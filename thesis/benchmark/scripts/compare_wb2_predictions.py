#!/home/ekasteleyn/aurora_thesis/aurora_env/bin/python
"""
Compare local Aurora predictions against WeatherBench2 Aurora reference.

This script compares the predictions generated by run_wb2_predictions.py
against the official WB2 Aurora reference to verify bit-exact reproducibility.

Usage:
    python compare_wb2_predictions.py --input-dir /scratch-shared/ekasteleyn/aurora_wb2_predictions
"""

import argparse
from pathlib import Path

import fsspec
import numpy as np
import pandas as pd
import xarray as xr

# WB2 Aurora reference
WB2_URL = "gs://weatherbench2/datasets/aurora/2022-1440x721.zarr"

# Variable mapping: our names -> WB2 names
VAR_MAP = {
    "t": "temperature",
    "u": "u_component_of_wind",
    "v": "v_component_of_wind",
    "z": "geopotential\t",  # Note: WB2 has a tab char in name
    "q": "specific_humidity\t",  # Note: WB2 has a tab char in name
    "2t": "2m_temperature",
    "10u": "10m_u_component_of_wind",
    "10v": "10m_v_component_of_wind",
    "msl": "mean_sea_level_pressure",
}

# Pressure levels in WB2
WB2_LEVELS = [500, 700, 850]


def compute_metrics(a: np.ndarray, b: np.ndarray) -> dict:
    """Compute error metrics between two arrays."""
    diff = a - b
    return {
        "mae": float(np.nanmean(np.abs(diff))),
        "rmse": float(np.sqrt(np.nanmean(diff**2))),
        "max_diff": float(np.nanmax(np.abs(diff))),
        "mean_a": float(np.nanmean(a)),
        "mean_b": float(np.nanmean(b)),
    }


def main():
    parser = argparse.ArgumentParser(
        description="Compare Aurora predictions against WB2 reference"
    )
    parser.add_argument(
        "--input-dir", type=str, default=None,
        help="Directory containing Aurora prediction .nc files (processes all matching files)"
    )
    parser.add_argument(
        "--files", type=str, nargs="+", default=None,
        help="List of specific Aurora prediction .nc files to process"
    )
    parser.add_argument(
        "--output-csv", type=str, default=None,
        help="Output CSV file for results"
    )
    args = parser.parse_args()
    
    if args.files:
        nc_files = [Path(f) for f in args.files]
        base_dir = nc_files[0].parent
    elif args.input_dir:
        base_dir = Path(args.input_dir)
        nc_files = sorted(base_dir.glob("aurora_pred_*.nc"))
    else:
        print("Error: Must provide either --input-dir or --files")
        return
        
    output_csv = Path(args.output_csv) if args.output_csv else base_dir / "comparison_results.csv"
    
    print("=" * 70)
    print("  AURORA vs WB2 PREDICTION COMPARISON")
    print("=" * 70)
    
    print(f"\nFound {len(nc_files)} local prediction files")
    
    if len(nc_files) == 0:
        print("No prediction files found!")
        return
    
    # Parse all files to find what init times and lead times we need
    needed_inits = set()
    needed_leads = set()
    for nc_path in nc_files:
        parts = nc_path.stem.split("_")
        date_str = parts[2]
        init_str = parts[3]
        lead_hours = int(parts[5].replace("h", ""))
        init_time = pd.Timestamp(f"{date_str[:4]}-{date_str[4:6]}-{date_str[6:8]} {init_str[:2]}:{init_str[2:]}")
        needed_inits.add(init_time)
        needed_leads.add(lead_hours)
    
    print(f"  Init times needed: {sorted(needed_inits)}")
    print(f"  Lead times needed: {sorted(needed_leads)}")
    
    # Open WB2 reference and preload just what we need
    print("\nLoading WB2 Aurora reference (preloading subset)...")
    wb2 = xr.open_zarr(fsspec.get_mapper(WB2_URL), chunks=None)
    
    # Select only the init times and lead times we need
    needed_inits_list = sorted(needed_inits)
    needed_leads_td = [np.timedelta64(h, 'h') for h in sorted(needed_leads)]
    
    # Preload atmospheric vars for comparison (t, u, v at 500, 700, 850 hPa)
    print("  Preloading t, u, v at levels 500, 700, 850...")
    wb2_atmos = wb2[["temperature", "u_component_of_wind", "v_component_of_wind"]].sel(
        time=needed_inits_list,
        level=WB2_LEVELS
    ).compute()
    print("  ✓ Atmospheric data loaded")
    
    # Preload surface vars
    print("  Preloading surface variables...")
    wb2_surf = wb2[["2m_temperature", "10m_u_component_of_wind", "10m_v_component_of_wind", "mean_sea_level_pressure"]].sel(
        time=needed_inits_list
    ).compute()
    print("  ✓ Surface data loaded")
    
    results = []
    
    for i, nc_path in enumerate(nc_files):
        print(f"  [{i+1}/{len(nc_files)}] {nc_path.name}", end=" ", flush=True)
        
        # Parse filename: aurora_pred_YYYYMMDD_HHMM_stepXX_XXXh.nc
        parts = nc_path.stem.split("_")
        date_str = parts[2]  # YYYYMMDD
        init_str = parts[3]  # HHMM
        step = int(parts[4].replace("step", ""))
        lead_hours = int(parts[5].replace("h", ""))
        
        # Compute init time
        init_time = pd.Timestamp(f"{date_str[:4]}-{date_str[4:6]}-{date_str[6:8]} {init_str[:2]}:{init_str[2:]}")
        lead_td = np.timedelta64(lead_hours, 'h')
        
        try:
            local = xr.open_dataset(nc_path)
            
            # Compare atmospheric variables at WB2 levels
            for our_var in ["t", "u", "v"]:
                if our_var not in local:
                    continue
                
                wb2_var = VAR_MAP[our_var]
                
                for level in WB2_LEVELS:
                    if level not in local.level.values:
                        continue
                    
                    # Get local data (should be 720 lats, same as WB2)
                    local_data = local[our_var].sel(level=level).values
                    
                    # Get WB2 data from preloaded dataset
                    wb2_data = wb2_atmos[wb2_var].sel(
                        time=init_time, 
                        prediction_timedelta=lead_td, 
                        level=level
                    ).values
                    
                    # WB2 might have different lat ordering - check and align
                    if wb2_atmos.latitude.values[0] < wb2_atmos.latitude.values[-1]:
                        # WB2 is S->N, local is N->S, flip WB2
                        wb2_data = wb2_data[::-1, :]
                    
                    metrics = compute_metrics(local_data, wb2_data)
                    
                    results.append({
                        "init_time": str(init_time),
                        "lead_hours": lead_hours,
                        "variable": our_var,
                        "level": level,
                        **metrics,
                    })
            
            # Compare surface variables
            for our_var in ["2t", "10u", "10v", "msl"]:
                if our_var not in local:
                    continue
                
                wb2_var = VAR_MAP[our_var]
                
                local_data = local[our_var].values
                    
                wb2_data = wb2_surf[wb2_var].sel(
                    time=init_time, 
                    prediction_timedelta=lead_td
                ).values
                
                # Check lat ordering
                if wb2_surf.latitude.values[0] < wb2_surf.latitude.values[-1]:
                    wb2_data = wb2_data[::-1, :]
                
                metrics = compute_metrics(local_data, wb2_data)
                
                results.append({
                    "init_time": str(init_time),
                    "lead_hours": lead_hours,
                    "variable": our_var,
                    "level": None,
                    **metrics,
                })
            
            local.close()
            print("✓")
            
        except Exception as e:
            print(f"⚠ {e}")
    
    # Create results DataFrame
    df = pd.DataFrame(results)
    
    if len(df) == 0:
        print("\n⚠ No matching times found!")
        return
    
    # Save raw results
    df.to_csv(output_csv, index=False)
    print(f"\nResults saved to: {output_csv}")
    
    # Print summary
    print("\n" + "=" * 70)
    print("  SUMMARY: Mean metrics by variable")
    print("=" * 70)
    
    var_summary = df.groupby("variable").agg({
        "mae": ["mean", "max"],
        "rmse": ["mean", "max"],
        "max_diff": ["mean", "max"],
    }).round(6)
    print(var_summary.to_string())
    
    print("\n" + "=" * 70)
    print("  SUMMARY: Mean RMSE by lead time")
    print("=" * 70)
    
    lead_summary = df.groupby("lead_hours")["rmse"].agg(["mean", "std", "max"]).round(6)
    print(lead_summary.to_string())
    
    # Assessment
    print("\n" + "=" * 70)
    print("  ASSESSMENT")
    print("=" * 70)
    
    max_rmse = df["rmse"].max()
    mean_rmse = df["rmse"].mean()
    max_diff = df["max_diff"].max()
    
    print(f"  Overall mean RMSE: {mean_rmse:.6e}")
    print(f"  Overall max RMSE:  {max_rmse:.6e}")
    print(f"  Overall max diff:  {max_diff:.6e}")
    
    if max_rmse < 1e-5:
        print("\n  ✓ EXCELLENT: Differences within numerical precision (< 1e-5)")
    elif max_rmse < 1e-3:
        print("\n  ✓ GOOD: Very small differences (< 1e-3)")
    elif max_rmse < 0.1:
        print("\n  ⚠ WARNING: Some differences detected (< 0.1)")
    else:
        print("\n  ⚠ SIGNIFICANT: Notable differences (>= 0.1)")
    
    wb2.close()


if __name__ == "__main__":
    main()
